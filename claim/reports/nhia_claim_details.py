logger = logging.getLogger(__name__)


HEADER_ANY_STATUS = -1
AVAILABLE_CLAIM_STATUSES = [
    Claim.STATUS_REJECTED,
    Claim.STATUS_ENTERED,
    Claim.STATUS_CHECKED,
    Claim.STATUS_PROCESSED,
    Claim.STATUS_VALUATED,
    HEADER_ANY_STATUS,
]

UNKNOWN_USER = "Unknown"


def calculate_age(dob, until: None):
    if not until:
        from core import datetime
        until = datetime.datetime.today()
    return until.year - dob.year - ((until.month, until.day) < (dob.month, dob.day))


def prepare_user_data(user_id: int, user_type: str):
    if not user_id:
        return None

    user = InteractiveUser.objects.filter(id=user_id).first()
    if not user:
        logger.error(f"Can't find Claim {user_type} with ID={user_id}")
        return UNKNOWN_USER

    return f"{user.other_names} {user.last_name}"


def prepare_product_data(items: QuerySet, services: QuerySet):
    for item in items:
        if item.product:
            return f"{item.product.code} - {item.product.name}"
    for service in services:
        if service.product:
            return f"{service.product.code} - {service.product.name}"
    return None


def prepare_claim_elements_data(elements: QuerySet):
    description = ""
    for element in elements:
        price = determine_price_element(element)
        name = element.itemsvc.name
        element_description = f"{name} - {price} / "
        description += element_description
    description = description[:-3]
    return description


def determine_price_element(element):
    if element.status == ClaimDetail.STATUS_REJECTED:
        return 0.0

    price = float(element.price_approved) if element.price_approved else float(element.price_asked)
    quantity = int(element.qty_approved) if element.qty_approved else int(element.qty_provided)
    final_price = price * quantity
    return final_price


def nhia_claim_details_query(user, start_date, end_date, requested_status=HEADER_ANY_STATUS, **kwargs):
    # Checking received parameters
    if start_date is None:
        return {"error": "Error - the start date must be selected"}
    if end_date is None:
        return {"error": "Error - the end date must be selected"}
    if start_date > end_date:
        return {"error": "Error - the start date cannot be greater than the end date"}
    claim_status = int(requested_status)
    if claim_status not in AVAILABLE_CLAIM_STATUSES:
        return {"error": f"Error - the requested status does not exist ({requested_status})"}

    header = {
        "start_date": start_date,
        "end_date": end_date,
        "requested_status": requested_status if requested_status else HEADER_ANY_STATUS,
    }
    report_data = {
        "header": [header],
        "nhia_logo": get_nhia_logo(),
        "requested_by": f"{user.username} - {user.other_names} {user.last_name}",
    }

    claim_filters = (Q(validity_to__isnull=True)
                     & Q(date_claimed__range=[start_date, end_date]))
    if claim_status != HEADER_ANY_STATUS:
        claim_filters &= Q(status=claim_status)

    claims = (Claim.objects.filter(claim_filters)
                           .prefetch_related("items")
                           .prefetch_related("services")
                           .prefetch_related("insuree")
                           .prefetch_related("health_facility")
                           .prefetch_related("icd")
                           .prefetch_related("admin")
                           .prefetch_related("items__item")
                           .prefetch_related("items__product")
                           .prefetch_related("services__service")
                           .prefetch_related("services__product")
                           .prefetch_related("insuree__family")
                           .prefetch_related("insuree__family__location")
                           .prefetch_related("insuree__family__location__parent")
                           .prefetch_related("insuree__family__location__parent__parent")
                           .order_by("date_from", "health_facility_id", "insuree__chf_id"))

    data = []
    paginator = Paginator(claims, 500)
    for page_number in paginator.page_range:
        page = paginator.page(page_number)

        for claim in page.object_list:
            hf = claim.health_facility
            insuree = claim.insuree
            family = insuree.family
            village = family.location
            ward = village.parent
            lga = ward.parent
            claim_admin = claim.admin
            diagnosis = claim.icd
            items = claim.items.filter(validity_to__isnull=True).all()
            services = claim.services.filter(validity_to__isnull=True).all()

            submitter_data = prepare_user_data(claim.audit_user_id_submit, "submitter")
            reviewer_data = prepare_user_data(claim.audit_user_id_review, "reviewer")
            processor_data = prepare_user_data(claim.audit_user_id_process, "processor")
            product_data = prepare_product_data(items, services)
            items_data = prepare_claim_elements_data(items)
            services_data = prepare_claim_elements_data(services)

            new_data_element = {
                "nin": insuree.chf_id,
                "insuree": f"{insuree.other_names} {insuree.last_name}",
                "lga": f"{lga.code} {lga.name}",
                "ward": f"{ward.code} {ward.name}",
                "village": f"{village.code} {village.name}",
                "address": insuree.family.address,
                "gender": insuree.gender_id,
                "age": calculate_age(insuree.dob, claim.date_from),
                "claim_admin": f"{claim_admin.code} - {claim_admin.other_names} {claim_admin.last_name}",
                "hf": f"{hf.code} {hf.name}",
                "hf_level": hf.level,
                "claim_code": claim.code,
                "claim_status": claim.status,
                "diagnosis": f"{diagnosis.code} - {diagnosis.name}",
                "date_from": claim.date_from,
                "date_to": claim.date_to if claim.date_to else claim.date_from,
                "visit_type": claim.visit_type,
                "product": product_data,
                "items": items_data,
                "services": services_data,
                "amount_claimed": claim.claimed,
                "date_submitted": claim.submit_stamp,
                "submitter": submitter_data,
                "feedback_status": claim.feedback_status,
                "review_status": claim.review_status,
                "date_reviewed": claim.validity_from_review,
                "reviewer": reviewer_data,
                "amount_approved": claim.approved if claim.audit_user_id_review else None,
                "date_processed": claim.process_stamp,
                "processor":  processor_data,
                "batch_run": claim.batch_run_id,
            }
            data.append(new_data_element)

    report_data["data"] = data
    return report_data
